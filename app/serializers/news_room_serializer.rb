class NewsRoomSerializer < ActiveModel::Serializer
  embed :ids
  attributes :id, :user_id, :company_name, :room_type, :size, :email, :phone_number, :fax, :web_address,
      :description, :address_1, :address_2, :city, :state, :postal_code, :country, :owner_name,
      :job_title, :facebook_link, :twitter_link, :linkedin_link, :instagram_link, :tags, :subdomain_name

  has_many :industries
end
